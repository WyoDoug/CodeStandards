    [Fact]
    public async Task STR0009_FlagsNestingDepthError()
    {
        var source = @"
class C
{
    void M()
    {
        if (true)
        {
            if (true)
            {
                if (true)
                {
                    {|#0:if (true)
                    {
                        {|#1:if (true)
                        {
                            {|#2:if (true)
                            {
                                {|#3:if (true)
                                {
                                }|}
                            }|}
                        }|}
                    }|}
                }
            }
        }
    }
}
";

        await AnalyzerTestUtilities.VerifyAnalyzerAsync<Str0009NestingDepthAnalyzer>(source,
                 AnalyzerTestUtilities.CreateResult(DiagnosticDescriptors.STR0009, locationIndex: 0, "4", "3"),
                 AnalyzerTestUtilities.CreateResult(DiagnosticDescriptors.STR0009, locationIndex: 1, "5", "3"),
                 AnalyzerTestUtilities.CreateResult(DiagnosticDescriptors.STR0009, locationIndex: 2, "6", "3"),
                 AnalyzerTestUtilities.CreateResult(DiagnosticDescriptors.STR0009Error, locationIndex: 3, "7", "6")
            );
    }

