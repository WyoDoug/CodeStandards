    [Fact]
    public async Task STR0001_FlagsContinueStatement()
    {
        var source = @"
class C
{
    void M()
    {
        for (int i = 0; i < 10; i++)
        {
            {|#0:continue;|}
        }
    }
}
";

        await AnalyzerTestUtilities.VerifyAnalyzerAsync<Str0001NoContinueAnalyzer>(source,
                 AnalyzerTestUtilities.CreateResult(DiagnosticDescriptors.STR0001, locationIndex: 0)
            );
    }

